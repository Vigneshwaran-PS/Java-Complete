package Control_Statement;

public class If_Statement_with_boolean {


    static boolean b;//false

    public static void main(String[] args) {
        if(b){
            System.out.println("true");
        }else{
            System.out.println("false");
        }

        if(!b){
            System.out.println("true");
        }else{
            System.out.println("false");
        }
    }
}
